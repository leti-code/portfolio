#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	char			*aux;

	aux = (char *)s;
	i = 0;
	while (i < n)
	{
		(*(aux + i)) = c;
		i++;
	}
	return (s);
}
